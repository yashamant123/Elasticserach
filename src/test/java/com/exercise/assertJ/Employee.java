package com.exercise.assertJ;

public class Employee {
	int age;
	String name;
	int salary;
	
	  Employee(int age,String name,int salary) {
		this.age=age;
		this.name=name;
		this.salary=salary;
	}
	public int getAge() {
		return age;
	}
	
	public String getName() {
		return name;
	}
	
	public int getSalary() {
		return salary;
	}
	
	

	
}
